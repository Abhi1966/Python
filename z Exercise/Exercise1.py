# Create a Calculator capable for perfoming addition, subtraction,
# multiplication and division operation on two number

a = input("Enter first number: ")
b = input("Enter second number: ")

print("the sum of", a, "and", b, "is:", float(a) + float(b))
print("the subtraction of", a, "and",b, "is:", float(a) - float(b))
print("the multiplication of", a, "and", b, "is:", float(a) * float(b))
print("the division of", a, "and", b, "is:", float(a) / float(b))